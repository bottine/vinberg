using LinearAlgebra

function ⊕(M1,M2)
    
    n1 = size(M1)[1]
    n2 = size(M2)[1]
    @assert size(M1) == (n1,n1)
    @assert size(M2) == (n2,n2)
    @assert M1' == M1
    @assert M2' == M2

    P = zeros(Int,n1+n2,n1+n2)
    P[1:n1,1:n1] = M1
    P[n1+1:end,n1+1:end] = M2

    return P
end

